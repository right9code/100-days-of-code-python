# Mathematical Operations in Python

# Addition: "+"
print(2 + 3)

# Subtraction:  "-"
print(2 - 3)

# Multiplication:   "*" ...
print(2 * 3)

# Division: "/" ; Remember disvision always give a float output...
print(2/3)

# Exponents/Raise powers: "**" ...
print(2**3)

