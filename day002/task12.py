# F-strings in Python
# To mention various data types into one string we use f string i.e. "f" in front of operation

#Here we combine a string, a float, an integer and a boolean into a string
name = "Abhi"
weight = 65.5
shoe_size = 7
isSexy = True

print(f"You are {name}, weighing around {weight} kgs with shoe size {shoe_size}UK and is very sexy = {isSexy}")